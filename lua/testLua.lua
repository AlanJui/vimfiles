-- Variable definition:
local a, b

-- Initialization
a=10
b=30

print("Initial variables...")
print("value of a:", a)
print("value of b:", b)
print("")

-- Swapping of variables
b, a = a, b

print("Swapping variables...")
print("value of a:", a)
print("value of b:", b)
print("")

-- Floating value
-- f = 70.0 / 3.0
f = 70 / 3.0
print("Floating value: f = 70 / 3.0 ...")
print("value of f", f)
